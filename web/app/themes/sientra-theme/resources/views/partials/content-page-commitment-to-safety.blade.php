    <div class="card">  
      <div class="card-body light">
        <h2>Capsular Contracture</h2>
        <p>During the healing process after surgery, in some women, the scar tissue (capsule) squeezes the implant. It can result in firmness or hardening of the breast. Through 10 Years, in general, a patient’s risk of capsular contracture is 13.5%.<sup>1</sup> This means 14 out of 100 patients may experience capsular contracture within 10 years after receiving implants.<sup>*</sup></p>
      </div>
    </div>

    <div class="card">  
      <div class="card-body light">
        <h2>Breast Implant-Associated Anaplastic Large Cell Lymphoma (BIA-ALCL)</h2>
        <p> BIA-ALCL is NOT breast cancer. BIA-ALCL is a rare and treatable type of non-Hodgkin T-cell lymphoma. Women with breast implants have a small but increased risk of developing this.<sup>2,3</sup></p>
        <p>Current data suggests that BIA-ALCL occurs more frequently in women with textured implants than smooth implants.<sup>4</sup> BIA-ALCL has been diagnosed in women with silicone and saline implants.<sup>5</sup> Worldwide, the reported incidence rates of BIA-ALCL vary widely and within different implant surfaces.6 When caught early, BIA-ALCL is usually curable.<sup>7</sup></p>
        <p>It is important to understand how rare the occurrence is and put the numbers into perspective. The risk of developing ALCL from a breast implant is 0.003%.<sup>8</sup>
        It is important for women to be advocates for their breast health and be aware of any changes in size or shape of their breasts or breast implants. For any woman who is concerned about their breast health, they should see their board-certified or board-eligible plastic surgeon for evaluation.<sup>7</sup></p>
        <p>Learn more about potential breast implant risk in augmentation and reconstruction</p>
      </div>
    </div>

